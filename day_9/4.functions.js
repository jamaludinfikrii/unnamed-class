// hitungUmur(2000) ==> 23
// hitungUmur(1999) ==> 24
function hitungUmur(tahunLahir) {
  var umur = 2023 - tahunLahir
  return `Lahir tahun ${tahunLahir}, umur sekarang ${umur}`
}

console.log(hitungUmur(1945))
console.log(hitungUmur(1999))

// parkir tiap jam 2000
// hitungJamParkir(2) ==> 4000

function hitungJamParkir(lamaParkir) {
  var totalHarga = lamaParkir * 2000
  return `total parkir = ${totalHarga}`
}

console.log(hitungJamParkir(5))



